//Arithmetic operations
let x = 30;
let y = 15;

//get sum or total '+'
let sum = x + y;
console.log("Sum:", sum);

//get difference '-'

let difference = x - y;
console.log("the difference of 30 - 15 is:", difference);

//get product '*'
let product = x * y;
console.log("the product of 30 * 15 is:", product);

//get quotient '/'

let quotient = x / y;
console.log("the quotient of 30 / 15 is:", quotient);

//get modulus '%'

let modulus = x % y;
console.log("the modulus of 30 % 15 is:", modulus);

//get modulus '%'
let remainder = x % y;
console.log("Remainder:", remainder);

//Concatenation
const firstWord = "Hello";
const secondWord = "World!";
console.log(firstWord + " " + secondWord);

//Logical Expression
let sunny = true;
let warm = false;

console.log("Is it sunny AND warm?", sunny && warm);
console.log("Is it sunny OR warm?", sunny || warm);
console.log("Is it NOT sunny?", !sunny);

